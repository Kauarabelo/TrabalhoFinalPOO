package br.com.estudoskaua.trabalhofinalpoo.domain.model;

/**
 * Enum que representa os tipos de veículos disponíveis em um leilão.
 * <p>
 * Os tipos são:
 * <ul>
 *     <li>CARRO: Veículo motorizado com quatro rodas.</li>
 *     <li>MOTOCICLETA_PASSEIO: Veículo motorizado de duas rodas, destinado ao transporte de passageiros.</li>
 *     <li>CAMINHÃO: Veículo motorizado grande, projetado para transporte de carga.</li>
 *     <li>UTILITÁRIOS: Veículos destinados a serviços específicos ou comerciais.</li>
 * </ul>
 * </p>
 *
 * @author Kaua
 */
public enum TipoVeiculo {
    CARRO,
    MOTOCICLETA_PASSEIO,
    CAMINHÃO,
    UTILITARIOS
}
