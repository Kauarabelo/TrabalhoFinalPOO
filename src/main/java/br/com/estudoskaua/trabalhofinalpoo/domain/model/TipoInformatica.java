package br.com.estudoskaua.trabalhofinalpoo.domain.model;

/**
 * Enum que representa os tipos de dispositivos de informática disponíveis em um leilão.
 * <p>
 * Os tipos são:
 * <ul>
 *     <li>NOTEBOOK: Dispositivo portátil com teclado e tela.</li>
 *     <li>MONITOR: Tela externa para visualização de computadores.</li>
 *     <li>HUB: Dispositivo que conecta múltiplos dispositivos em uma rede.</li>
 *     <li>SWITCH: Dispositivo de rede que conecta dispositivos e encaminha pacotes de dados.</li>
 *     <li>ROTEADOR: Dispositivo que direciona pacotes de dados entre redes.</li>
 * </ul>
 * </p>
 *
 * @author Kaua
 */
public enum TipoInformatica {
    NOTEBOOK,
    MONITOR,
    HUB,
    SWITCH,
    ROTEADOR
}
